#define _CRT_SECURE_NO_WARNINGS 
#include <stdio.h>

void task09()
{
    // 도시 이름 배열 (출력용)
    char* city[] = { "서울", "수원", "천안", "대전", "대구", "부산" };

    // 요금표 2차원 배열
    // [행:출발지][열:도착지]
    // 빈칸은 0으로 채우거나, 대각선 대칭 구조를 활용해도 됨
    // 여기서는 문제의 표 구조대로, 출발지 번호가 도착지 번호보다 작은 경우만 값 입력
    int toll_table[6][6] = {
        {0, 1700, 4200, 7500, 15200, 19300}, // 서울 출발
        {0, 0,    3400, 6700, 13600, 17800}, // 수원 출발
        {0, 0,    0,    4200, 11000, 15000}, // 천안 출발
        {0, 0,    0,    0,    7800,  12100}, // 대전 출발
        {0, 0,    0,    0,    0,     4500 }, // 대구 출발
        {0, 0,    0,    0,    0,     0    }  // 부산 출발
    };

    int start_code, end_code;
    int temp; // swap용 변수

    printf("고속도로 통행료 계산 program\n\n");

    // 도시별 번호 안내 출력
    printf("각 도시별 번호\n");
    printf("서울(0) 수원(1) 천안(2)\n");
    printf("대전(3) 대구(4) 부산(5)\n\n");

    // 1. 입력
    printf("출발지 도시번호 입력하고 Enter>");
    scanf("%d", &start_code);

    printf("도착지 도시번호 입력하고 Enter>");
    scanf("%d", &end_code);

    // 예외 처리: 잘못된 도시 코드 입력 시 종료
    if (start_code < 0 || start_code > 5 || end_code < 0 || end_code > 5)
    {
        printf("잘못된 도시 번호입니다.\n");
        return 0;
    }

    // 2. 처리 (요금 조회)
    // 표는 출발지 코드 < 도착지 코드 인 경우만 값이 채워져 있음.
    // 만약 부산(5) -> 서울(0) 처럼 출발지 코드가 더 크다면, 
    // 서울(0) -> 부산(5) 요금과 동일하므로 두 값을 바꿔서 조회해야 함.

    int s = start_code;
    int e = end_code;

    if (s > e)
    {
        // swap: s와 e의 값을 맞바꿈
        temp = s;
        s = e;
        e = temp;
    }
    else if (s == e)
    {
        // 같은 도시로 이동 시 요금 0원
        printf("출발지와 도착지가 같습니다.\n");
        return 0;
    }

    // 3. 출력
    // cities 배열을 이용해 도시 이름 출력, table 배열을 이용해 요금 출력
    printf("%s에서 %s까지 요금 : %d 원\n", city[start_code], city[end_code], toll_table[s][e]);
}

int main()
{
    task09();
    return 0;
}